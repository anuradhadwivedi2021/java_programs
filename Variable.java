import java.lang.*;

public class Variable {
    public static void main(String[] args) {
        int age = 22;
        float marks = 78.8f;
        char grade = 'A';
        boolean passed = true;

        System.out.println("Age:" + age);
        System.out.println("Marks:" + marks);
        System.out.println("Grade:" + grade);
        System.out.println("Passed:" + passed);
        
    }
}